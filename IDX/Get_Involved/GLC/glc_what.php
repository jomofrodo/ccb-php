<h1>What is the Good Life Club? </h1>
<p>The Good Life Club program is focused on the specifc needs of children living 
  in crisis situations in the confict zones of Burma and incorporates the provision 
  of emergency aid into a program that is fun, educational and focused on physical 
  and spiritual health. </p>
<p>The Good Life Club program works through Free Burma Ranger relief teams working 
  in confict areas of Burma. It is comprised of men and women of different ethnicities 
  and faiths and the program provides assistance to all people regardless of race, 
  ethnicity or religion. The team believes that good life is not just physical 
  but emotional and spiritual as well, and is something God desires for all people 
  everywhere. They try to meet the physical, emotional and spiritual needs of 
  the different communities they visit. </p>
<p>Even though there are Buddhist and Animist team members in addition to Christians, 
  all agree with the possibility of abundant life even in the midst of hardship, 
  and individuals' ability to help share this life. The GLC is based on the words 
  of Jesus in John 10:10: "For the thief comes only to steal and kill and destroy, 
  but I have come that you might have life and have it abundantly." The aim of 
  the program is to strengthen the spirit of the people, encouraging them to persevere 
  in building a new Burma with the tools of love, truth, courage, freedom and 
  reconciliation. </p>
