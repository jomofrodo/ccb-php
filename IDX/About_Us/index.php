<h1>CHRISTIANS CONCERNED FOR BURMA (CCB) </h1>
<h3>Christians Concerned for Burma (CCB), works to support the people of Burma 
  through prayer, sharing the love of Jesus Christ, advocacy and action. </h3>
<p><span class="heading2">Vision: </span>To be a part of God's Kingdom in Burma, 
  to see people in a relationship with Jesus Christ, and to support political 
  freedom, justice, liberty and reconciliation in Burma. </p>
<p><span class="heading2">Mission:</span> To encourage people to pray and act 
  for Burma, to share the good news of Jesus Christ and to help those in need. 
</p>
<h2>Activities:</h2>
<ol>
  <li> To facilitate the annual Global Day of Prayer for Burma the second Sunday 
    of March every year. The CCB encourages people all over the world pray and 
    act for the Burma. Throughout the year the CCB produces newsletters and information 
    updates on the situation in Burma. For the Global Day of Prayer for Burma, 
    CCB coordinates global prayer and advocacy activities as well as providing 
    resources for those who participate. Churches in over 55 countries now participate 
    in the Global Day of Prayer for Burma. CCB website: (www.prayforburma.org 
    )</li>
  <li>To share the love of Jesus Christ.</li>
  <li>To support the Good Life Club (GLC) program for women and Children of Burma. 
    The Good Life Club is based on the Bible verse; John 10:10, and the belief 
    that God has abundant life for all of us regardless of our circumstances. 
    <p> The Good Life Club is a spiritual, counseling and educational program 
      for children and mothers. It is also a way that young people around the 
      world can connect with people in Burma by sending post cards and small presents 
      of needed items. T-shirts, hats, and Ziploc bags with vitamins, combs, toothpaste 
      and toothbrush, nail clippers, baby clothing, toys, messages from the senders 
      and other items are given to IDP children and mothers with babies. Free 
      Burma Ranger relief teams distribute these to people displaced by attacks 
      of the Burma Army and teach basic health care to the displaced.</p>
  </li>
  <li> To support Wa (now 1) and Karen (now 1) ethnic boarding school for children 
    in Thailand.</li>
  <li>To support ethnic students (15) to attend Bible schools in Thailand.</li>
  <li>To support ethnic students (2) to attend University in Thailand</li>
  <li> To support Bible translation and scripture printing and distribution for 
    the Wa, Karen, Karenni and Shan languages.</li>
  <li>To support ethnic Wa and Karen evangelists (10) and teachers (11) in Thailand 
    and Burma</li>
  <li>To support churches in Thailand and Burma (numerous). </li>
</ol>
