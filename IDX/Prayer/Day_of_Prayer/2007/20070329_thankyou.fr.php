<h1>JOUR DE PRIERE POUR LA BIRMANIE </h1>
<h3>Mars 2007</h3>
<p>Chers amis, 
<p> Merci pour vos prières en faveur du peuple de Birmanie, particulièrement 
  à l'occasion de la Journée Mondiale de Prière pour la Birmanie du 11 mars 
  2007. On a également prié en Birmanie et voici des photographies d'un groupe 
  de Karens victimes de déplacement interne qui, en compagnie d'une organisation 
  de jeunesse karen, se sont rassemblés dans leur village pour prier. 
