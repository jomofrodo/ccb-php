<p>Avant la réunion de prière, l'équipe a distribué aux enfants des ballons, 
  et dressé une grande banderole portant l'inscription: "Jour de Prière pour 
  la Birmanie: 11 mars 2007". 
<p> C'était une belle scène: ces gens chantant en choeur au sommet d'une colline 
  caressée par le souffle d'une légèrre brise, tout environnés de hautes montagnes! 
<p> Les assistants ont prié et nous avons tous rendu grâce à Dieu de ce que 
  des habitants du monde entier priaient eux aussi au même moment. Les difficultés 
  peuvent bien advenir, et les oppresseurs être en apparence si puissants: Dieu 
  nous aidera à endurer, à tout surmonter, et à vivre chaque journée avec 
  espoir et joie. 
<p> Un peu auparavant le même jour, nous avions rencontré une famille qui nous 
  a raconté la tentative de viol perpétrée par l'armée birmane sur leur nièce 
  âgée de 13 ans. Leur village est très proche d'un camp de l'armée birmane 
  et ils reçoivent régulièrement la visite de soldats... 
<p> Ils nous ont rapporté qu'une quinzaine de jours auparavant, un soldat birman 
  avait tenté de violer trois femmes la même nuit. La première lui a résisté, 
  aussi lui a-t-il lancé son couteau. Elle a été atteinte et blessée en profondeur 
  à la cheville mais est parvenue à s'échapper. Le soldat s'est alors rendu 
  dans une autre maison, y est entré et a empoigné une femme qui était là 
  avec son bébé. Elle a réussi à se dégager et s'échapper. Il a alors saisi 
  le bébé en lui disant: "J'ai ton bébé: tu n'as plus qu'à revenir!" Le bébé 
  s'est mis à crier, et le soldat l'a posé pour aller dans une troisième maison. 
  A l'intérieur, il a trouvé une jeune fille de 13 ans endormie. Il a tenté 
  de la violer, mais elle s'est réveillée et a résisté à coups de pieds en 
  criant à l'aide. Ses père et mère qui étaient dans la pièce voisine, et 
  ses oncle et tante (sa tante était la femme blessée un peu plus tôt par le 
  lancer de couteau), sont venus à la rescousse avant que le soldat ait pu en 
  venir à ses fins. Devant la famille réunie le soldat a pris la fuite. Ces 
  villageois étaient terrorisés à l'idée d'être attaqués par l'armée birmane 
  en représailles de leur résistance et disaient leur indignation de ne pouvoir 
  être reçus par le commandant au sujet de cette affaire. 
