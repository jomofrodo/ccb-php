<p>Sa famille a déclaré: "Nous sommes très heureux de votre venue à tous, 
  et d'avoir pu vous raconter ce qui est arrivé. La situation ici est très dangereuse, 
  et nous ne savons ce que l'armée birmane a l'intention de faire la prochaine 
  fois. Noius voulons que le monde connaisse notre situation: Que Dieu vous bénisse, 
  revenez s'il vous plaît!" 
<p> La jeune fille, pour sa part, a dit: "Merci d'être venus, et pour moi, je 
  vais bien". 
<p> Nous sommes heureux que la jeune fille n'ait pas été violée, mais en même 
  temps tristes pour ces gens contraints de vivre perpétuellement sous la menace. 
  Nous sommes retourné<br>
  s à l'endroit où nous devions nous joindre à la journée de prière et j'ai 
  pensé que c'était là une des nombreuses raisons pour lesquelles nous prions 
  pour un changement en Birmanie. Et tandis que nous priions, il me revint à 
  l'esprit que nous avons de l'espoir parce que, qu'importe ce qui arrive en ce 
  monde, Dieu nous a préparé une place. Cela signifie que nous avons la possibilité 
  de faire le bon choix même si cela implique de perdre, puisque nos vies avec 
  Dieu sont éternelles. 
