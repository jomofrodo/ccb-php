<h2>Global Day of Prayer for Burma, 2008 </h2>
<h1>Human Dignity </h1>
<p>The Free Burma Rangers (FBR) is a multi-ethnic humanitarian service movement. 
  They bring help, hope and love to internally displaced people under attack in 
  the war zones of Burma. Teams are trained, supplied and sent into the areas 
  under attack to provide emergency medical care, shelter, food, clothing and 
  human rights documentation. The teams also operate a communication and information 
  network inside Burma that provides real time information from areas under attacks. 
  Together with other groups, the teams work to serve people in need. The letter 
  below is from a doctor working with the Free Burma Rangers. It attempts to answer 
  the questions of why and how to be involved in Burma: 
<p>I am writing from the jungles of eastern Burma where I am teaching advanced 
  medics with the Free Burma Rangers (FBR). In the publication A Campaign of Brutality 
  (<a href="http://www.freeburmarangers.org/Features/20070315_NorthernKarenState.pdf">http://www.freeburmarangers.org/Features/20070315_NorthernKarenState.pdf</a> 
  ), FBR gives 10 reasons why it is right to be involved in Burma. The very first 
  listed is for human dignity. In a land such as Burma, the value of a human life 
  becomes more than some distant theoretical discussion. It becomes a serious 
  and very personal issue. 
<p>As I struggled with the idea of human dignity, I decided to ask the medics 
  what they thought. One of the medics, named Raykaw, answered, “Once, because 
  God gave us love, so we need to love each other… to help each other. That is 
  our dignity.” I then asked him if that would apply to an injured Burma Army 
  soldier who came to him for treatment. At first, Raykaw looked at me as if he 
  didn’t understand the question. Slowly, thoughtfully, he replied: “For me, if 
  [a Burma Army soldier] came to me… I want to help them. They need love and help. 
  For example, when we see them, if they have no one to help them to stop their 
  bleeding, they will need something to keep their life. For me, we need to help 
  them. If they are bad after that, no problem… That’s up to them.” These are 
  the words of a very experienced medic, one who has seen the atrocities of the 
  Burma Army with his own eyes. One who has spent years risking his life to bring 
  medical help to people hiding from the Burma Army in the jungle. One who, as 
  a child, was himself displaced from his home by these very soldiers. For Raykaw, 
  his human dignity is to be able to give to others, even to his enemies. Period. 
  His profound statement is backed up by his life as a Free Burma Ranger medic. 
<p>Giving is clearly one aspect of God’s own nature. As beings made in His image, 
  giving becomes one aspect of our own human dignity. Even if, as Raykaw said, 
  it means giving to an enemy who would turn right around and do us harm. I have 
  come to believe that our human dignity is wrapped up in our ability to exhibit 
  any of our maker’s characteristics. Generous giving. Creativity. Love. Courage. 
  Kindness. Free choice. And to do it solely because it is of God… because it 
  is truly good. If so, then it is our duty, our honor, even our dignity as Christians 
  to enable and protect the capacity of all men to exhibit those characteristics 
  in their lives. 
<p>This is the reason I believe it is right and critically important as a Christian 
  to be involved in Burma. Just as the Free Burma Rangers describe… “for human 
  dignity.” For me, not only does this answer the why to be involved, it also 
  addresses the how to be involved. “Once, because God gave us love, so we need 
  to love each other… to help each other.” That is our dignity—even to love our 
  enemies. 
