          <td> <span class="greytext">Letter from a Partners team member helping 
            the FBR to train Good Life Club counselors: </span> 
            <p>“Yes, I know what that feels like,” said Kaw Bla Sei. “My mother 
              and I were captured by the Burma Army when I was 5 years old. For 
              a whole day they dragged us through the jungle without any food 
              and water. Then they let us go. After that happened I was always 
              afraid. It did not go away until I turned 12.” </p>
            <p>The group I was speaking to was the Good Life Counselors. They 
              are the men and women who will focus on the needs of children when 
              they, together with the rest of the relief team they are a part 
              of, enter a village or an IDP site. “Why are the children important?” 
              I ask them. “Because they are the future!” is the answer I get back. 
              “What is the most important quality you need to serve the children?” 
              “We must love them.” On their journey to the villages and hide sites 
              of the displaced people, they will try to find ways to meet both 
              the physical and spiritual needs. 
            <p>After a class is over Kaw Bla Sei says, “A while ago I found four 
              children aged 4 to 10 wandering aimlessly around a village. They 
              did not have any parents. I helped them get to a refugee camp where 
              they were taken in at an orphanage. They are all doing really well 
              now.” 
            <p>Saw Kiaw has chosen to follow Jesus’ calling to serve the little 
              children, and I believe there will be a real, special prize for 
              people like him. Until then he will be busy looking for children 
              in need. 
