        <p>Dear Friends,<br>
          Thank you for sending gifts for the children of Burma. The Good Life 
          Club Kid-Packs, Mom&amp;Baby Packs, smile-hats and T-shirts have been 
          given to hundreds of children since 2001. These displaced children have 
          been blessed by your generosity and thoughtfulness. If you would like 
          to receive a short DVD about the Good Life Club, please contact us.<br>
        </p>
        <p>Using the supplies from the packs, we are able to give the newly trained 
          relief teams resources to teach villagers about good hygiene and nutrition. 
          We've even included a basic health-care presentation in the team's supplies, 
          such as puppets and a script for education and entertainment. The packs 
          are distributed constantly as the relief teams continue to travel throughout 
          the war-torn areas of Burma.<br>
        </p>
        <p>The situation of the Internally Displaced People of Burma is still 
          dire and in need of our love, prayers, and relief supplies. Your gifts 
          for the children prove to them that they are not forgotten and that 
          people around the world do care and want to be involved in their lives. 
          The kind messages, prayers, and bible verses bring hope that God will 
          be with them daily and meet their needs. The children of Burma may one 
          day see an end to the war that has held this country captive for 50 
          years. Thank you again for keeping alive the belief that they are valuable 
          and can live each day in hope of<br>
          an abundant life.</p>
