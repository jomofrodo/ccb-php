        <p> The children study Math, History, Wa, Burmese, English and Art. There 
          aren't enough teachers to offer science according to Roi Ji. The government 
          now provides one teacher to the hostel school. Pastor John said it's 
          a joy to see them singing hymns and songs of praise learned by heart. 
          The hostel children are using a spring at the bottom of the gully for 
          washing their clothes. There is a small pump sending drinking and household 
          water up to the hostel, but no storage tank. They would like to build 
          a water storage tank that could provide some irrigation water for their 
          3 acres of corn and 2 acres of new tea trees. They also have two pigs 
          and a few chickens. They want to produce more of their own food. This 
          year the United Nations school feeding program (UNWFP) has provided 
          rice for the school children who attend 80% of the time during the school 
          year (10 kilograms per child per month).
