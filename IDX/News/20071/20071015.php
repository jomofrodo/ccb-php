              <p> <br>
                I feel like I gave my body, heart, and soul to be able to go to 
                visit Shan IDPs on the border. I began getting in shape for this 
                trip over 9 months ago. Not exactly easy when you're 54 years 
                old and have never done regular aerobic exercise. By June, I progressed 
                to hiking the 11 km. road up the mountain near my home every Saturday. 
                With each plodding step I prayed for the people of Burma and for 
                my poor, struggling body (sometimes in reverse order). In August 
                and September, when I left the "easy" road hikes to begin doing 
                the jungle trail up the same mountain once or twice every week, 
                I thought about my physical agony with each panting breath and 
                mentally labored to keep my focus on the goal of passing the physical 
                endurance requirements set by Partners. 
              <p> In mid-September, after a 10-hour hike, I arrived at the training 
                site. That is when I met this woman and the baby. The woman had 
                been invited by the FBR to share her story. When the baby smiled 
                at me, two small teeth gleamed in the sunshine. When the woman 
                smiled, one solitary tooth stood like a lone sentinel at the left 
                corner of her mouth. 
