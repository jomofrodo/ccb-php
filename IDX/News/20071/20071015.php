              <p> Teeth. One good way to do a quick assessment among the IDPs 
                of Burma is to count teeth. Many, by the time they are 30 or 40 
                years old, have less teeth than the fingers on one hand. Counting 
                teeth is like receiving a full report in 2 seconds about poverty, 
                malnutrition, lack of education and dental hygiene, and lack of 
                access to health care. 
              <p> But though this woman tried to smile with her mouth closed to 
                hide her toothlessness, her lone tooth had spoken to me. 
              <p> <i>What can I give…poor (?) as I am? </i> 
              <p> My answer: I'm giving a lot, Lord. Look at all I did to get 
                here: All those preparatory hikes. All that time exercising in 
                between. All the expenses: good hiking shoes, energy bars, clothes 
                made of fast-drying fabric. And don't forget that 10-hour hike 
                to get here! And now, I'm still giving: giving up electricity, 
                a warm shower, a soft mattress. 
              <p> 
              <p><i> What can I give…poor as I am? </i> 
              <p> <br>
                This woman's answer: <i>I'll give this baby my love and a place 
                in my home and my heart. </i> 
              <p> <br>
                Raped and impregnated by a Burmese army soldier, the baby's mother 
                died from "complications" 10 days after giving birth. But here 
                is this 45-year-old poor, toothless woman with three children 
                ages 8-13. She certainly has enough to worry about. Living on 
                a meager food supply without a steady income in an IDP camp under 
                the noses of the hate-filled Burmese army—does it make any sense 
                to take in one more mouth to feed? She is not a relative. She 
                is under no obligation. Yet those chubby baby cheeks just begging 
                to be kissed declare, "Look how much she care about me! I'm well-fed; 
                I'm clean; I'm loved." 
              <p> <br>
                So in a few months, when I again sing this Christmas carol, I'll 
