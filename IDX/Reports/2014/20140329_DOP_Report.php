<b>Albany, New York, USA:</b> Meredith (a Karenni friend who recently relocated to Albany) hosted the first Day of Prayer for Burma here in Albany. Despite having a really large population of Karen (1000+) here, no one had done it before. So it much pleased Meredith to host it here in the Baptist church, and we also made a special prayer for Burma, and a special prayer for you. We made 15 copies of the report on Burma that you emailed and we distributed them to community leaders and area churches.<p>
