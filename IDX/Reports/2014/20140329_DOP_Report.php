<b>Gig Harbor, Washington, USA:</b> Chapel Hill Presbyterian Church, Global Day of Prayer for Burma and Run for Relief-Burma - Running Free, March 9, 2014: Thank you all - participants and volunteers - for your prayers for the people of Burma and for making the 11th annual Run for Relief - Burma: Running Free event so meaningful.  God provided beautiful weather, great conversations and a new awareness about the needs of the people from Burma. We had almost 300 participants including the Boy Scouts and many refugees from Burma.   Funds raised provide medical and humanitarian support for internally displaced families from Burma.  This event is on the same day as the Global Day of Prayer for Burma.<p>
