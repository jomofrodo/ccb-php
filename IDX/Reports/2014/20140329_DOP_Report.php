<b>Lancaster, Pennsylvania, USA:</b> On March 9, 2014, in observance of Global Day of Prayer for Burma, the Pennsylvania Rangers set up an informational table with display boards, printed information, Karen clothing, and pictures at the Eastern Mountain Sports store in the Park City Mall of Lancaster, PA. This display was open to the public throughout the afternoon of March 9, 2014. It gathered much interest. There was great positive feedback.  Most people commented that they did not know anything of the current situation and would pray for Burma. This was organized and created by Sean Fitzgerald and local Karen refugees- members of the Pennsylvania Rangers.  All members joined in prayer and fasted as a show of solidarity for Burma.<p>
