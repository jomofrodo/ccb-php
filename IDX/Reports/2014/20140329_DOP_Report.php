Dear friends, <p>
 <p>
Thank you for praying with us for Burma on Sunday 9 March 2014.  People all across the world joined together to remember the suffering of the people from Burma; to celebrate the work God has done there at all levels, from working transformation in the lives of individuals to reformation of systems; and to pray for freedom and reconciliation, true peace and justice, for all the people of Burma.  <p>
 <p>
As we look back at the long history of wrongs, of abuse and oppression and atrocity, and look at the situation now and the headlong pursuit of development at all costs, the prospects of both true justice and deep reconciliation seem dim.  Yet, in Psalm 85 we are assured that "righteousness and peace have kissed," that "truth will spring from the earth and righteousness shall look down from heaven." And so we trust God that as we seek Him for answers, for guidance, in our desperation and in our celebration, He will fulfill his promises. <p>
 <p>
Below are several reports from friends around the world who prayed with us.  We know many others prayed too; your prayers are needed and we are grateful. <p>
 <p>
Thank you and God bless you,<p>
 <p>
Christians Concerned for Burma<p>
