I looked up and at first saw only a few people coming forward. Then one by
one, two by two, then more and more started walking forward. Some were
crying, some were smiling, all came with the look of  purpose on their
faces. Soon more than a thousand people had come forward to give themselves
to Jesus. Counselors were with them up front to pray and it was a full and
happy throng.  By the end of the festival, over 4,000 people had made a commitment
to Jesus.
<p>
Later that night Derek and the BGEA staff gave us all the funds needed for
the transport and feeding of the refugees.
<p>
The next day after the 10 van load of refugees left our home to return to
their camp, I was grateful for God's power, timing and His love. "Good bye,
thank you, thank you, God bless you!" the choir called out as they smiled,
waved and disappeared around the bend. I am grateful for the choir, to all
who enabled them to get to the festival, to the Thai authorities, to the
church leaders of Thailand, to the BGEA and to God who cares for us all.
<p>
Love, Dave and family


