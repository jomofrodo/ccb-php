<p>
The next day Pastor Simon, the founder and leader of Mae La Refugee Camp
Bible School, was in Chiang Mai. He had received a special pass to get out
of the refugee camp to attend a church meeting there. I asked him to come to
my house and told him about the Festival and asked him to come. "Why not
have the choir come too?" My wife Karen asked. This had never been done
before.
<p>
"Wow, that will take a big miracle, but let's pray and try."

<p>
I called Derek of BGEA right away and we prayed together on the phone. He
said, "Usually we plan these events well in advance, but let's try. Achan
Buacab, the Thai person in charge of all the local groups who will be
performing, will need to agree, but if she does we will do it."
<p>
"Yes, I know her, she is a good friend." I called Achan Buacab and asked her
about the choir and prayed with her.
<p>
"Yes, we have a slot that just came open," she said.
<p>
I told Pastor Simon the good news. He thanked God with me and said he would
need a letter from the BGEA to try to get permission for some of his choir
to come. The BGEA team and local Thai
church leaders, lead by Dr. Kiat, prepared the document. I left the day
after that for a relief mission and asked Hosannah on our team to help
coordinate the next steps.
<p>

A few days before the event was to start I heard that the permission was not
yet granted. In the camp where we were training, we all joined in prayer
each night for God to open the way for the choir to go to Chaing Mai and
attend the festival.
<p>
With only three days before the event, I asked Hosi to ask mom and dad (they
have been missionaries here for over 50 years), to help. They contacted a
Thai general friend who has helped us before for prostheses for landmine
victims.  He immediately contacted the local and regional authorities who
gave the initial permission needed but there were still other steps to take.
<p>
Rev. Kiat recalls the day before the event being awakened at midnight to
pray for the choir to make it. Then friends in Bangkok made a special effort
to get permission all the way through. It worked and at midnight, the start
of the first day of the festival, the choir, all 130 of them, arrived at our
house. We thanked God for all who helped get them there and for the
generosity and care of the Thai authorities at every level.
<p>
We came back off the mission for this, arriving home just before the refugee
choir. Hosi, Kit, Micah and the staff helped them fill every bit of our
home, the bunk house, Laurie's house and all the space in between. Also at
our house was the ACTs team, a 20-person church group led by Jeff Sutton
from the US.  They immediately volunteered to help us feed and take of the
refugee choir and served the first meal at 2 am.  Later on the Esh family
made a special spaghetti dinner for all.
