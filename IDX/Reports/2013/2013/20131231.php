Dear friends,
<p>
Thanks for your prayers and help. Recently the Billy Graham Evangelistic
Association (BGEA), held an Abundant Life Festival in Chiang Mai. Leading up
to the festival I met with a friend named Eleanor who said, "It would be
great if some refugees could come to the event."
<p>
"Great idea," I said. "It would take a miracle, but we will pray and try."
<p>
