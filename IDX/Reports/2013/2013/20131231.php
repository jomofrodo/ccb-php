<p>
The Karen choir sang with great power and beauty and lifted all of us up
closer to the Lord. It was beautiful and I thanked God. Later one of the of
the Thai generals who helped us said, "I am grateful that God used me to do
something good. It was a privilege to be part of God's actions. It is
wonderful the choir made it here."
<p>
The festival itself was a powerful time of God's love and presence and I saw
thousands come forward to receive Christ. In all our years in Thailand we
have never seen any response of this scale. The morning of the last day, the
ACTS group from the US, performed a three-minute silent  play for the choir
at our home. It was about how we people can look for love and belonging in
the wrong places and be disappointed and how people can turn on us and
betray us, how Jesus comes to help, allows Himself to be abused and killed,
yet rises from the grave, scatters the darkness and saves us.
