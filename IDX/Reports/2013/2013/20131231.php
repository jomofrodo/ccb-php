I was deeply moved by the play and prayed with Karen that somehow, even at
this late hour, we could have the group perform this play at the festival
this last night.
<p>
Later that day, Bob, from Decision magazine, came out to interview the choir
and helped me connect with JW, who was running the festival production. I
asked him about adding the play and he said, "Yes, let's go for it, as long
as our Thai partners are ok with the last minute change."
<p>
I called Achan Buacab again, we discussed this and prayed and she said, "Yes
we will find a way!"  Later that night I watched as thousands cheered the performance of
both choir and play.
<p>
On this last night Franklin spoke the simple, clear and powerful gospel
message of our need for forgiveness and how Jesus wants to forgive us, that
God sent Jesus to save us and help us be the people we were made to be. At
the  time of invitation, I  felt stirred to pray against the powers of
darkness and for Franklin's message and God's Spirit to penetrate to all
hearts here.
