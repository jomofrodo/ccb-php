<em>7 March, 2018</em>

<p>Dear praying friends,

<p>Thank you for joining us on March 11 in praying for Burma. This past year the people of Burma have suffered many injustices; the murder, rape, and burning of villages and displacement of over 700,000 Rohingya people, the ongoing attacks against the Kachin, Ta’ang and northern Shan, and the lack of care by the government for people who have been wronged.

<p>In some ways Burma has improved. There is a civilian-elected government and more freedoms in some areas, but the military holds ultimate power and uses it. We pray for the military to have a change of heart and for the elected government to have the courage to stand for all who are oppressed. God has taught us lessons that we want to share in Burma and everywhere.

<p>First, wherever Jesus sends you, He goes with you. Second, when we are faced with great evil, Jesus will work through our weakness and bring good from the clutches of evil. Third, vengeance is often an attempt to get justice in a spirit of hate and retribution but justice can only be achieved with love and mercy. We need God’s help to love those who wrong us, to love our enemies – and when we pray and ask for this love He helps us. Four, we experienced the power of the praying church and believe that God’s power through prayer is the only way to achieve freedom, justice, reconciliation and love.

<p>The way forward in Burma is to pray and ask Jesus to help us love and help us know where to go, how to go and what to do. Thank you for praying with us for all the people of Burma.

<p>God bless you,

<p>Dave Eubank 
<br/>Christians Concerned for Burma


