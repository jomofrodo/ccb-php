<h1>Message from a Nurse:</h1>
<h3>15 Octobler, 2007</h3>
<p><i><span class="greytext">Dear friends , </span></i></p>
<p><span class="greytext"><i>This is from a nurse, Cindy, who helped us train 
  new Shan medics in a border area last month. I hope her message is an encouragement 
  to you,</i> </span>
<p><span class="greytext"><i>God bless you, the FBR team</i> </span>
<hr>
<p>One of my favorite old English carols has a verse that goes like this:
<p><i>What can I give Him, poor as I am?<br>
  If I were a shepherd, I would bring a lamb;<br>
  If I were a wise man, I would do my part;<br>
  But what I can, I give Him—give Him my heart. </i>
<p><br>
  I feel like I gave my body, heart, and soul to be able to go to visit Shan internally 
  displaced persons (IDPs) on the border. The Free Burma Rangers (FBR), a humanitarian 
  releif organization that works in the conflict areas of Burma and Partners Relief 
  and Development were conducting a joint training of Shan FBR releif teams. I 
  was invited to participate. I began getting in shape for this trip over 9 months 
  ago. Not exactly easy when you're 54 years old and have never done regular aerobic 
  exercise. By June, I progressed to hiking the 11 km. road up the mountain near 
  my home every Saturday. With each plodding step I prayed for the people of Burma 
  and for my poor, struggling body (sometimes in reverse order). In August and 
  September, when I left the "easy" road hikes to begin doing the jungle trail 
  up the same mountain once or twice every week, I thought about my physical agony 
  with each panting breath and mentally labored to keep my focus on the goal of 
  passing the physical endurance requirements set by Partners.
<p>&nbsp;
