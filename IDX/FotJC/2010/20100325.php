          <h2>Dear friends and family, </h2>
          <p><font size="3">Thank you for partnering with us for love, faith, freedom, 
            justice and reconciliation in Burma. This year we were reminded of 
            the power of prayer, the freedom of obedience, and the love of God. 
            With your help we now have 53 multi- ethnic Free Burma Ranger relief 
            teams who serve those in need. </font></p>
          <p><font size="3">While on a recent relief mission, the Burma Army moved 
            to attack five villages. We divided our teams and moved between the 
            Burma Army and the villagers. There were already thousands displaced 
            and our teams had just buried a man decapitated by the troops. I prayed 
            for three things; first, by God's power the Burma Army would turn 
            back, second that the villagers and our teams would not be hurt and 
            third- this I did not want to pray - but felt led to; that the Burma 
            Army would not be hurt and just go back. I was angry at the Burma 
            army and I also wanted to have a part in stopping them, but I knew 
            I should pray for them. For four days we kept moving to keep between 
            the Burma Army and the villagers as the Burma Army kept changing course 
            until they went back to their base. No shot was fired and no one was 
            hurt. All we did was pray, move and stand with the people. </font>
          <p><font size="3">Last year we also visited friends in the US and Afghanistan 
            and we thank you for your love, prayers, support for us, our wonderful 
            support team and the teams in the field. </font>
          <p><font size="3">God bless you, Dave, Karen, Sahale, Suu, Peter and 
            the teams </font> 
